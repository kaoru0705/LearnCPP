#include <iostream>
#include <random>

class character {
public:
    character() {
        std::cout << "character 클래스 생성자" << std::endl;
    };
};

class monster {
public:
    monster() {
        std::cout << "monster 클래스 생성자" << std::endl;
    };
};

//몬스터 A는 기본 Monster 클래스로부터 상속
class monster_a : public monster, character {
public:
    monster_a() : monster_a(10, 10) {
        std::cout << "monster_a 클래스 생성자" << std::endl;
    };

    monster_a(int x, int y) : location{ x, y } {
        std::cout << "monster_a 클래스 생성자 (매개변수 추가)" << std::endl;
    };

    void show_location() {
        std::cout << "위치(" << location[0] << " , " << location[1] << ")" << std::endl;
    };

private:
    int location[2];
};

int main() {
    monster_a forest_monster;
    forest_monster.show_location();

    return 0;
}